<h2>Bulimia nerviosa</h2>
<p>La bulimia nerviosa, llamada frecuentemente «bulimia», es un trastorno de la alimentación grave y potencialmente mortal. Cuando padeces bulimia, tienes episodios de atracones y purgas que incluyen la sensación de pérdida de control sobre tu alimentación. Muchas personas con bulimia también restringen lo que comen durante el día, lo que suele causar más episodios de atracones y purgas.

Durante estos episodios, es normal que consumas una gran cantidad de alimentos en un tiempo corto, para luego intentar deshacerte de las calorías extra de una manera poco saludable. Debido a la culpa, la vergüenza y el temor intenso a aumentar de peso por comer en exceso, puedes provocarte vómitos, puedes ejercitarte desmesuradamente o puedes usar otros métodos, como los laxantes, para deshacerte de las calorías.

Si tienes bulimia, probablemente te preocupe tu peso y tu figura corporal, y tal vez te juzgues con severidad y dureza por los defectos que son producto de tu autopercepción. Puedes tener un peso normal o, incluso, un poco de sobrepeso.</p>

