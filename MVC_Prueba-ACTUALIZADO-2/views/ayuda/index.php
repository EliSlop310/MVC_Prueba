<h3>Trastornos alimenticios.</h3>
<p>Los síntomas varían en función del tipo de trastorno de la alimentación. La anorexia nerviosa, la bulimia nerviosa y el trastorno alimentario compulsivo son los trastornos de la alimentación más frecuentes. Otros trastornos de la alimentación comprenden el trastorno de rumiación y el trastorno por evitación o restricción de la ingesta de alimentos.</p>
