<h2>Obesidad</h2>
<p>Obesidad significa tener un exceso de grasa en el cuerpo. Se diferencia del sobrepeso, que significa pesar demasiado. El peso puede ser resultado de la masa muscular, los huesos, la grasa y/o el agua en el cuerpo. Ambos términos significan que el peso de una persona es mayor de lo que se considera saludable según su estatura.

La obesidad se presenta con el transcurso del tiempo, cuando se ingieren más calorías que aquellas que quema. El equilibrio entre la ingestión de calorías y las calorías que se pierden es diferente en cada persona. Entre los factores que pueden afectar su peso se incluyen la constitución genética, el exceso de comida, el consumo de alimentos ricos en grasas y la falta de actividad física.

La obesidad aumenta el riesgo de padecer diabetes, enfermedades cardiacas, derrames cerebrales, artritis y ciertos cánceres. Si usted está obeso, perder por lo menos de cinco a 10 por ciento de su peso puede retrasar o prevenir algunas de estas enfermedades. Por ejemplo, si usted pesa 200 libras, el cinco al 10 por ciento serían unas 10 a 20 libras.</p>
