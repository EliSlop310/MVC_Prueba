<h2>Sobrepeso</h2>
<p>El sobrepeso es el aumento de peso corporal por encima de un patrón dado y para evaluar si una persona presenta sobrepeso, los expertos emplean una fórmula llamada índice de masa corporal (IMC), que calcula el nivel de grasa corporal en relación con el peso y estatura.​

Se considera normal un IMC entre 18,5 a 24,9.

Los adultos con un IMC de 25 a 29,9 se consideran con sobrepeso. No obstante, algunas personas en este grupo pueden tener mucho peso muscular y por lo tanto no tanta grasa, como es el caso de los atletas. En estas personas su peso no representa un aumento asociado del riesgo de problemas de salud.

Los adultos con un IMC de 30 a 39,9 se consideran obesos y con un IMC igual o superior a 40, extremadamente obesos. Cualquier persona con más de 45 kg de sobrepeso se considera que sufre de obesidad mórbida.1</p>
