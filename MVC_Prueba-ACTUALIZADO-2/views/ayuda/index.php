<h2>Anorexia nerviosa</h2>
<p> La anorexia nerviosa, a menudo simplemente denominada «anorexia», es un trastorno de la alimentación potencialmente mortal que se caracteriza por un peso corporal anormalmente bajo, un gran temor a aumentar de peso y una percepción distorsionada del peso o de la figura corporal. Las personas con anorexia hacen todo lo posible por controlar el peso y la figura corporal, lo que frecuentemente afecta de manera importante la salud y las actividades cotidianas.

Cuando tienes anorexia, limitas en exceso la ingesta de calorías o usas otros métodos para bajar de peso; por ejemplo, te ejercitas de forma desmesurada, tomas laxantes o suplementos dietéticos, o vomitas después de comer. Los esfuerzos para bajar de peso, incluso cuando el peso corporal es bajo, pueden causar problemas de salud graves al punto de morirse de hambre.</p>
